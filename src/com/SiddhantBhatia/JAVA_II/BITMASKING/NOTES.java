package com.SiddhantBhatia.JAVA_II.BITMASKING;

public class NOTES {
    public static void main(String[] args) {
        int a = 0 ^ 0;
        int b = 0 ^ 1;
        int c = 1 ^ 0;
        int d = 1 ^ 1;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.print(d);
    }

}
